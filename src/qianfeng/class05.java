package qianfeng;

public class class05 {
    public static void main(String[] args) {
        /*
        switch case when
         */
        int week = 5;
        switch (week){
            case 1:
                System.out.println("周一");
                break;
            case 2:
                System.out.println("周二");
                break;
            default:
                System.out.println("输入有误！");
        }
    }
}
